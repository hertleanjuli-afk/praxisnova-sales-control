const PAGE_TITLES: Record<string, string> = {
  '/': 'Dashboard',
  '/leads': 'Lead-Suche',
  '/sequences': 'Sequenzen',
  '/inbound': 'Eingehende Leads',
  '/linkedin': 'LinkedIn-Liste',
  '/clicks': 'Website Clicks',
  '/errors': 'Fehler-Log',
  '/settings': 'Einstellungen',
  '/ads': 'TikTok / Ads',
};

export default function DashboardLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const { data: session, status } = useSession();
  const pathname = usePathname();
  const router = useRouter();

  useEffect(() => {
    if (status === 'unauthenticated') {
      router.push('/login');
    }
  }, [status, router]);

  if (status === 'loading') {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-50">
        <div className="flex flex-col items-center gap-3">
          <svg
            className="h-8 w-8 animate-spin text-[#2563EB]"
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
          >
            <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4" />
            <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4z" />
          </svg>
          <p className="text-sm text-gray-500">Laden...</p>
        </div>
      </div>
    );
  }

  if (status === 'unauthenticated') {
    return null;
  }

  const pageTitle = PAGE_TITLES[pathname] ?? 'PraxisNova';

  return (
    <div className="min-h-screen flex bg-gray-50">
      {/* Sidebar */}
      <aside className="fixed inset-y-0 left-0 w-[250px] bg-[#1E3A5F] text-white flex flex-col z-30">
        {/* Logo */}
        <div className="px-5 py-6 border-b border-white/10">
          <h1 className="text-lg font-bold tracking-tight">PraxisNova AI</h1>
          <p className="text-xs text-blue-200 mt-0.5">Sales Control Center</p>
        </div>

        {/* Navigation */}
        <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
          {navItems
            .filter((item) => !item.tiktokOnly || tiktokEnabled)
            .map((item) => {
              const isActive = pathname === item.href;
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  className={`flex items-center gap-3 px-3 py-2.5 rounded-md text-sm font-medium transition-colors ${
                    isActive
                      ? 'bg-blue-600 text-white'
                      : 'text-blue-100 hover:bg-white/10 hover:text-white'
                  }`}
                >
                  {item.icon}
                  {item.label}
                </Link>
              );
            })}
        </nav>

        {/* User / Logout */}
        <div className="px-4 py-4 border-t border-white/10">
          <p className="text-sm font-medium text-white truncate">
            {session?.user?.name ?? session?.user?.email ?? 'Benutzer'}
          </p>
          <button
            onClick={() => signOut({ callbackUrl: '/login' })}
            className="mt-2 flex items-center gap-2 text-xs text-blue-200 hover:text-white transition-colors"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
            Abmelden
          </button>
        </div>
      </aside>

      {/* Main content */}
      <div className="ml-[250px] flex-1 flex flex-col min-h-screen">
        {/* Top header */}
        <header className="sticky top-0 z-20 bg-white border-b border-gray-200 px-6 py-4">
          <h2 className="text-lg font-semibold text-[#1E3A5F]">{pageTitle}</h2>
        </header>

        {/* Page content */}
        <main className="flex-1 p-6">{children}</main>
      </div>
    </div>
  );
}
